# а программа - определять количество вхождений одной строки в другую.

# для одного символа второй строки

print('1) Выполнение c встроенной функцией <text1.count(text2)>')
text1 = 'привет миру мир привет'
print('Текст № 1: ', text1)
text2 = 'р'
print('Текст № 2: ', text2)
print(f'В тексте <{text2}> повторяется: ', text1.count(text2))

